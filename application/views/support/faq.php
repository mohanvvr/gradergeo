        <a name="question6"></a>
        <div class="question_text">
        	<p>What counts as a correct answer?</p>
        </div>
        <div class="content">
        <p>The student's response must match the key for the answer to receive credit. If the student bubbles more than one choice or the student leaves the question unanswered, the student does not receive credit. GraderJoe will circle correct responses with a green oval and incorrect responses with a red oval. Questions unanswered or with multiple answers selected will be highlighted with a yellow rectangle. (<a href="#top" class="textlink2">back to questions</a>)</p>
        </div>
        
        <a name="question7"></a>
        <div class="question_text">
        	<p>Must a pencil be used to fill the bubbles?</p>
        </div>
        <div class="content">
        <p>Either a pen, marker or a pencil may be used. For best results, ensure that the students make full, dark marks to mark the answer ovals.(<a href="#top" class="textlink2">back to questions</a>)</p>
        </div>
        
        <a name="question8"></a>
        <div class="question_text">
        	<p>How can a student correct a mistake?</p>
        </div>
        <div class="content">
        <p>If using a pencil, the student may cleanly erase the mistake and bubble the desired choice. If using a pen, the student may use whiteout to cover the mistake and then bubble the desired choice.(<a href="#top" class="textlink2">back to questions</a>)</p>
        </div>
        
        <a name="question9"></a>
        <div class="question_text">
        	<p>What if the student ID is shorter than 10 digits in length?</p>
        </div>
        <div class="content">
        <p>If a students ID is 1234, the student may bubble any four consecutive columns in the ID block with 1234. It is NOT necessary for the leading zeros to be bubbled. (<a href="#top" class="textlink2">back to questions</a>)</p>
