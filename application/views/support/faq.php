        	<p>Why must stray marks be erased?</p>
        </div>
        <div class="content">
        <p>Unlike some other systems, GraderJoe uses an optical method to grade tests. This allows for any pencil or pen to be used as long as the mark is dark. However, if there are stray marks on an answer sheet, GraderJoe may not be able to grade it. (<a href="#top" class="textlink2">back to questions</a>)</p>
