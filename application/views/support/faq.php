        <a name="question3"></a>
        <div class="question_text">
        	<p>What kind of scanner do I need?</p>
        </div>
        <div class="content">
        <ul class="faq_li">
        	<li>Your scanner must have an Automatic Document Feeder (ADF) with the capacity to handle as many sheets as the number of students in your largest class. Most consumer and basic office multi-function printer-scanners have ADF capacities ranging from 15 to 35 sheets.</li>
            <li>You scanner must be compatible with your computer operating system.</li>
        </ul>    
        <p>Note : Many office supply stores will allow you to return your scanner within 14 days of purchase, with receipt. Check with your store when purchasing your scanner.<p>
        <div class="faq_div">(<a href="#top" class="textlink2">back to questions</a>)</div>
