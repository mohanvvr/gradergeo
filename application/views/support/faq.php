        <a name="question5"></a>
        <div class="question_text">
        	<p>What scanner settings should I use?</p>
        </div>
        <div class="content">
       <ul class="faq_li">
       		<li>Set the scanner to "Grayscale" or "Black and White Photo" or equivalent. Do not scan in color. </li>
            <li>Set the resolution to 100 dpi or 150 dpi or 200 dpi or 300 dpi, preferably 100 dpi.</li>
            <li>Set the scan type to jpg or jpeg.</li>
       </ul> 
